package Iterator.CE_bonus1;

public class NhatKy {
    String tenNK;
    String moTa;

    public NhatKy(String tenNK, String moTa) {
        this.tenNK = tenNK;
        this.moTa = moTa;
    }

    @Override
    public String toString() {
        return "NhatKy{" +
                "tenNK='" + tenNK + '\'' +
                ", moTa='" + moTa + '\'' +
                '}';
    }
}
